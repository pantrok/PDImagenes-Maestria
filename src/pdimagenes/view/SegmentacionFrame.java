    private void init() {
        
        sensarRoi = false;
        
        rois = new ArrayList<>();
        
        jLabel2.setIcon(new ImageIcon(imagen));
        jLabel2.addMouseListener(new MouseListener() {
            @Override
            public void mouseClicked(MouseEvent e) {
                if (sensarRoi) {
                    System.out.println("Sensando puntos");
                    if (p1 == null) {
                        p1 = new Point(e.getX(), e.getY());
                    } else if (p2 == null) {
                        p2 = new Point(e.getX(), e.getY());
                        //Calcular el x y y mas pequeño y cual es la distancia entre las x y las y para tener el ancho y alto
                        int x = p1.getX() <= p2.getX() ? (int)p1.getX() : (int)p2.getX();
                        int y = p1.getY() <= p2.getY() ? (int)p1.getY() : (int)p2.getY();
                        int w = Math.abs((int)p1.getX() - (int)p2.getX());
                        int h = Math.abs((int)p1.getY() - (int)p2.getY());
                        //Una vez que se tienen los valores se hace la subimagen
                        BufferedImage subimagen = imagen.getSubimage(x, y, w, h);
                        Roi roi = new Roi(subimagen);
                        rois.add(roi);
                        //Se pinta en el text area la roi marcada con su punto x, y y su ancho y alto
                        String strRoi = "Roi x = " + x + " y = " + y + " w = " + w + " h = " + h;
                        jTextArea1.setText(jTextArea1.getText() == null ? strRoi : (jTextArea1.getText() + "\n" + strRoi));
                        //Finalmente se limpian los puntos a null
                        p1 = null; p2 = null;
                        //Se pone bandera en false para no capturar puntos
                        sensarRoi = false;
                    }
                }
            }

            @Override
            public void mousePressed(MouseEvent e) {
                //
            }

            @Override
            public void mouseReleased(MouseEvent e) {
                //
            }

            @Override
            public void mouseEntered(MouseEvent e) {
                //
            }

            @Override
            public void mouseExited(MouseEvent e) {
                //
            }
        });

    }

