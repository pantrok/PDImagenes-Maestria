    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if (rois.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe definir al menos un roi", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        //Llamar metodo de clase segmentacion
        Segmentacion seg = new Segmentacion(imagen, rois);
        imagenSegmentada = seg.segmentarRois();
        jLabel2.setIcon(new ImageIcon(imagenSegmentada));
        
    }//GEN-LAST:event_jButton4ActionPerformed

